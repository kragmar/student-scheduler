const mongoose = require("mongoose");

const studentSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    birthDate: Date,
  },
  {
    timestamps: true,
  }
);

mongoose.model("Student", studentSchema);
